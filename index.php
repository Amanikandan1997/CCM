<script>
// Target date and time (Nov 1, 2025, 00:00:00)
const targetDate = new Date('November 1, 2025 00:00:00').getTime();

function updateTimer() {
  const now = new Date().getTime();
  const timeLeft = targetDate - now;

  if (timeLeft <= 0) {
    document.getElementById('countdown').textContent = 'Kumari Day Start!';
    clearInterval(timerInterval);
    return;
  }

  const days = Math.floor(timeLeft / (1000 * 60 * 60 * 24));
  const hours = Math.floor((timeLeft % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
  const minutes = Math.floor((timeLeft % (1000 * 60 * 60)) / (1000 * 60));
  const seconds = Math.floor((timeLeft % (1000 * 60)) / 1000);

  document.getElementById('countdown').textContent = 
    `CCM Award 2025 & Kumari Day Celebration 2025
க்கு இன்னும் ${String(days).padStart(2, '0')} நாட்கள் ${String(hours).padStart(2, '0')} 
மணிநேரம் ${String(minutes).padStart(2, '0')} 
நிமிடம் ${String(seconds).padStart(2, '0')} 
வினாடிகள் உள்ளன`;
}

// Update the timer every second
const timerInterval = setInterval(updateTimer, 1000);

// Initial call to display the timer immediately
updateTimer();
</script>
